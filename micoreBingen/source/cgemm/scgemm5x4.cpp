    V_MAC_F32(c[1][2].y,a[0][2].y,neg(b[0][1].x));
    V_MAC_F32(c[1][3].y,a[0][3].y,neg(b[0][1].x));
    V_MAC_F32(c[2][0].y,a[0][0].y,neg(b[0][2].x));
    V_MAC_F32(c[2][1].y,a[0][1].y,neg(b[0][2].x));
