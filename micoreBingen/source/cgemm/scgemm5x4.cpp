    V_MAC_F32(c[1][3].y,a[1][3].x,b[1][1].y);
    V_MAC_F32(c[2][0].x,a[1][0].x,b[1][2].x);
    V_MAC_F32(c[2][0].y,a[1][0].x,b[1][2].y);
    V_MAC_F32(c[2][1].x,a[1][1].x,b[1][2].x);
